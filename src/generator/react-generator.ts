import * as fs from 'node:fs/promises';
import * as path from 'node:path';
import * as crypto from 'node:crypto';
import type {
  AnimationsOutput,
  AssetManifest,
  DOMNode,
  DomExtractResult,
  HoverDiff,
} from '../types/index.js';

export type GeneratorFormat = 'vite' | 'nextjs';

export interface GenerateReactAppOptions {
  format?: GeneratorFormat;
}

export async function generateReactApp(
  outputDir: string,
  options: GenerateReactAppOptions = {},
): Promise<void> {
  const format: GeneratorFormat = options.format ?? 'vite';
  if (format === 'nextjs') {
    throw new Error(
      'Next.js output is not implemented yet — re-run with --format vite for now.',
    );
  }

  const dom = await readJson<DomExtractResult>(path.join(outputDir, 'dom-tree.json'));
  const animations = await readJson<AnimationsOutput>(path.join(outputDir, 'animations.json'));
  const manifest =
    (await readJson<AssetManifest>(path.join(outputDir, 'assets', 'manifest.json'))) ?? {};

  if (!dom) throw new Error('dom-tree.json not found — run the extractor first');

  const urlToPublic = new Map<string, string>();
  for (const [url, rec] of Object.entries(manifest)) {
    urlToPublic.set(url, '/' + rec.localPath.replace(/\\/g, '/'));
  }

  const appearIds = new Set(animations?.whileInViewCandidates ?? []);
  const hoverBySelector = new Map<string, HoverDiff>();
  for (const h of animations?.hoverStates ?? []) hoverBySelector.set(h.selector, h);

  const body = findBody(dom.tree) ?? dom.tree;
  const components = collectComponents(body);

  // Split each top-level body child into its own SectionN component so Page.tsx
  // stays well under Babel's per-function JSX size threshold. Each section is
  // emitted as an independent file with its own registered classes.
  const sections: ComponentDefinition[] = [];
  let sectionIdx = 1;
  for (const child of body.children) {
    if (SKIP_TAGS.has(child.tag)) continue;
    if (components.idToName.has(child.id)) continue;
    if (countElements(child) < 3) continue;
    const name = `Section${sectionIdx++}`;
    sections.push({ name, node: child });
    components.idToName.set(child.id, name);
  }

  const componentsDir = path.join(outputDir, 'src', 'components');
  await fs.mkdir(componentsDir, { recursive: true });

  const styleRegistry = newStyleRegistry();
  const allDefs = [...components.definitions, ...sections];

  for (const def of allDefs) {
    const ctx = newCtx(
      urlToPublic,
      appearIds,
      hoverBySelector,
      components.idToName,
      styleRegistry,
    );
    const jsx = emit(def.node, ctx, 2, { skipRootSubstitution: true });
    const file = renderComponentFile(def.name, jsx, ctx, false);
    await fs.writeFile(path.join(componentsDir, `${def.name}.tsx`), file);
  }

  // Emit Page component
  const pageCtx = newCtx(
    urlToPublic,
    appearIds,
    hoverBySelector,
    components.idToName,
    styleRegistry,
  );
  const pageJsx = emit(body, pageCtx, 2, { skipRootSubstitution: true });
  const pageFile = renderComponentFile('Page', pageJsx, pageCtx, true);
  await fs.writeFile(path.join(componentsDir, 'Page.tsx'), pageFile);

  // Generated stylesheet with all extracted style classes
  const stylesDir = path.join(outputDir, 'src', 'styles');
  await fs.mkdir(stylesDir, { recursive: true });
  await fs.writeFile(path.join(stylesDir, 'generated.css'), renderGeneratedCss(styleRegistry));

  // Copy assets → public
  await copyDir(path.join(outputDir, 'assets'), path.join(outputDir, 'public', 'assets'));

  // Scaffold project files
  await writeScaffold(outputDir);
}

function countElements(node: DOMNode): number {
  let n = 1;
  for (const c of node.children) n += countElements(c);
  return n;
}

// ---------- Style registry ----------

interface StyleRegistry {
  classByKey: Map<string, string>;
  ordered: Array<{ className: string; declarations: string }>;
}

function newStyleRegistry(): StyleRegistry {
  return { classByKey: new Map(), ordered: [] };
}

function registerStyle(
  reg: StyleRegistry,
  style: Record<string, string>,
): string | null {
  const keys = Object.keys(style);
  if (keys.length === 0) return null;
  keys.sort();
  const declarations = keys.map((k) => `${k}: ${style[k]}`).join('; ');
  const existing = reg.classByKey.get(declarations);
  if (existing) return existing;
  const hash = crypto.createHash('sha1').update(declarations).digest('hex').slice(0, 8);
  const className = `fe-${hash}`;
  reg.classByKey.set(declarations, className);
  reg.ordered.push({ className, declarations });
  return className;
}

function renderGeneratedCss(reg: StyleRegistry): string {
  const lines = [
    '/* Generated by framer-extract — deduped style classes extracted from computed styles */',
    '',
  ];
  for (const { className, declarations } of reg.ordered) {
    lines.push(`.${className} { ${declarations}; }`);
  }
  return lines.join('\n') + '\n';
}

// ---------- Context ----------

interface GenCtx {
  urlToPublic: Map<string, string>;
  appearIds: Set<string>;
  hoverBySelector: Map<string, HoverDiff>;
  componentMap: Map<string, string>;
  componentRefs: Set<string>;
  hasMotion: boolean;
  styleRegistry: StyleRegistry;
}

function newCtx(
  urlToPublic: Map<string, string>,
  appearIds: Set<string>,
  hoverBySelector: Map<string, HoverDiff>,
  componentMap: Map<string, string>,
  styleRegistry: StyleRegistry,
): GenCtx {
  return {
    urlToPublic,
    appearIds,
    hoverBySelector,
    componentMap,
    componentRefs: new Set(),
    hasMotion: false,
    styleRegistry,
  };
}

