// ---------- Component collection ----------

interface ComponentDefinition {
  name: string;
  node: DOMNode;
}

interface Components {
  definitions: ComponentDefinition[];
  idToName: Map<string, string>;
}

function collectComponents(root: DOMNode): Components {
  const byKey = new Map<string, string>();
  const definitions: ComponentDefinition[] = [];
  const idToName = new Map<string, string>();
  const usedNames = new Set<string>();

  function visit(node: DOMNode): void {
    if (node.framer?.isComponentRoot) {
      const rawName = componentNameFromNode(node);
      if (rawName) {
        const key = node.framer.componentIdentifier ?? rawName;
        let name = byKey.get(key);
        if (!name) {
          let candidate = rawName;
          let i = 2;
          while (usedNames.has(candidate)) candidate = `${rawName}${i++}`;
          name = candidate;
          usedNames.add(name);
          byKey.set(key, name);
          definitions.push({ name, node });
        }
        idToName.set(node.id, name);
      }
    }
    for (const c of node.children) visit(c);
  }

  visit(root);
  return { definitions, idToName };
}

function componentNameFromNode(node: DOMNode): string | null {
  const f = node.framer;
  if (!f) return null;
  const raw = f.name || f.componentIdentifier;
  if (!raw) return null;
  const cleaned = raw.replace(/[^a-zA-Z0-9]/g, '');
  if (!cleaned) return null;
  const pascal = cleaned.charAt(0).toUpperCase() + cleaned.slice(1);
  return /^[0-9]/.test(pascal) ? `C${pascal}` : pascal;
}

function findBody(node: DOMNode): DOMNode | null {
  if (node.tag === 'body') return node;
  for (const c of node.children) {
    const hit = findBody(c);
    if (hit) return hit;
  }
  return null;
}

// ---------- JSX emission ----------

interface EmitOpts {
  skipRootSubstitution: boolean;
}

function emit(node: DOMNode, ctx: GenCtx, indent: number, opts: EmitOpts): string {
  if (SKIP_TAGS.has(node.tag)) return '';

  const compName = ctx.componentMap.get(node.id);
  if (compName && !opts.skipRootSubstitution) {
    ctx.componentRefs.add(compName);
    return `${pad(indent)}<${compName} />`;
  }

  const pad1 = pad(indent);
  const useMotion = !!node.framer?.appearId && ctx.appearIds.has(node.framer.appearId);
  if (useMotion) ctx.hasMotion = true;
  const baseTag = node.tag;
  const tagName = useMotion ? `motion.${baseTag}` : baseTag;

  const { tailwindClasses, inlineStyle } = convertStyles(node.computedStyle, ctx.urlToPublic);
  const classParts = [...tailwindClasses];
  const generatedClass = registerStyle(ctx.styleRegistry, inlineStyle);
  if (generatedClass) classParts.push(generatedClass);
  const attrParts: string[] = [];

  for (const [k, v] of Object.entries(node.attributes)) {
    if (k === 'data-extract-id') continue;
    if (k.startsWith('data-framer-')) continue;
    if (k.startsWith('on')) continue;
    if (k === 'style') continue;
    if (k === 'class') {
      if (v) classParts.unshift(v);
      continue;
    }
    if (k === 'src' || k === 'href' || k === 'poster') {
      const resolved = ctx.urlToPublic.get(v) ?? v;
      attrParts.push(`${jsxAttrName(k)}=${jsxAttrValue(resolved)}`);
      continue;
    }
    if (k === 'srcset') {
      attrParts.push(`srcSet=${jsxAttrValue(rewriteSrcset(v, ctx.urlToPublic))}`);
      continue;
    }
    attrParts.push(`${jsxAttrName(k)}=${jsxAttrValue(v)}`);
  }

  if (classParts.length > 0) {
    attrParts.unshift(`className=${jsxAttrValue(classParts.join(' '))}`);
  }

  if (useMotion) {
    attrParts.push('initial={{ opacity: 0, y: 20 }}');
    attrParts.push('whileInView={{ opacity: 1, y: 0 }}');
    attrParts.push('viewport={{ once: true, margin: "-10%" }}');
    attrParts.push('transition={{ duration: 0.6, ease: "easeOut" }}');
  }

  const headAttrs = attrParts.length > 0 ? ' ' + attrParts.join(' ') : '';

  if (VOID_TAGS.has(node.tag)) {
    return `${pad1}<${tagName}${headAttrs} />`;
  }

  const childStrings: string[] = [];
  if (node.text) {
    childStrings.push(`${pad(indent + 1)}${escapeJsxText(node.text)}`);
  }
  for (const child of node.children) {
    const s = emit(child, ctx, indent + 1, { skipRootSubstitution: false });
    if (s) childStrings.push(s);
  }

  if (childStrings.length === 0) {
    return `${pad1}<${tagName}${headAttrs} />`;
  }

  return `${pad1}<${tagName}${headAttrs}>\n${childStrings.join('\n')}\n${pad1}</${tagName}>`;
}

function pad(n: number): string {
  return '  '.repeat(n);
}

function jsxAttrName(k: string): string {
  return ATTR_RENAME[k] ?? k;
}

function jsxAttrValue(v: string): string {
  if (v.includes('"') || v.includes('\n') || v.includes('\\')) {
    return `{${JSON.stringify(v)}}`;
  }
  return `"${v}"`;
}

function escapeJsxText(s: string): string {
  if (/[{}<>]/.test(s)) return `{${JSON.stringify(s)}}`;
  return s.replace(/&/g, '&amp;');
}

function rewriteSrcset(value: string, urlToPublic: Map<string, string>): string {
  return value
    .split(',')
    .map((entry) => {
      const trimmed = entry.trim();
      if (!trimmed) return '';
      const parts = trimmed.split(/\s+/);
      const url = parts[0]!;
      const rest = parts.slice(1).join(' ');
      const resolved = urlToPublic.get(url) ?? url;
      return rest ? `${resolved} ${rest}` : resolved;
    })
    .filter(Boolean)
    .join(', ');
}

function rewriteCssUrls(value: string, urlToPublic: Map<string, string>): string {
  return value.replace(/url\(\s*(['"]?)([^'")]+)\1\s*\)/g, (_m, q: string, u: string) => {
    const resolved = urlToPublic.get(u) ?? u;
    return `url(${q}${resolved}${q})`;
  });
}

function convertStyles(
  cs: Record<string, string>,
  urlToPublic: Map<string, string>,
): { tailwindClasses: string[]; inlineStyle: Record<string, string> } {
  const tailwindClasses: string[] = [];
  const inlineStyle: Record<string, string> = {};

  for (const prop of STYLE_ALLOWLIST) {
    const raw = cs[prop];
    if (raw == null || raw === '') continue;
    if (STYLE_DEFAULTS[prop]?.includes(raw)) continue;

    const tw = TAILWIND_MAP[prop]?.[raw];
    if (tw) {
      tailwindClasses.push(tw);
      continue;
    }

    const value =
      prop === 'background-image' || prop === 'background'
        ? rewriteCssUrls(raw, urlToPublic)
        : raw;
    inlineStyle[prop] = value;
  }

  return { tailwindClasses, inlineStyle };
}

// ---------- File rendering ----------

function renderComponentFile(name: string, jsx: string, ctx: GenCtx, isPage: boolean): string {
  const lines: string[] = [];
  if (ctx.hasMotion) lines.push(`import { motion } from "framer-motion";`);
  for (const comp of ctx.componentRefs) {
    if (comp !== name) lines.push(`import ${comp} from "./${comp}";`);
  }
  if (lines.length > 0) lines.push('');
  lines.push(`export default function ${name}() {`);
  lines.push(`  return (`);
  lines.push(jsx);
  lines.push(`  );`);
  lines.push(`}`);
  lines.push('');
  // Silence unused when the page has no embedded components
  if (isPage && ctx.componentRefs.size === 0 && !ctx.hasMotion) {
    // nothing extra
  }
  return lines.join('\n');
}

